package Conditions;

public class SwitchCase {

	public static void main(String[] args) {
		int age = 60;

		switch (age) {
		case 90:
			System.out.println("Congrats! turned to 90");
			break;
		case 80:
			System.out.println("Congrats! turned to 80");
			break;
		case 70:
			System.out.println("Congrats! turned to 70");
			break;
		case 60:
			System.out.println("Congrats! turned to 60");
			break;
		default:
			System.out.println("You are still pretty Young");

		}

	}

}
